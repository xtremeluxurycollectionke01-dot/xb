async function seedTimeManagement() {
    try {
        // Connect to MongoDB
        await mongoose.connect(MONGODB_URI);
        console.log('✅ Connected to MongoDB');

        // Clear existing data
        console.log('🧹 Clearing existing data...');
        await Promise.all([
            TimeRecord.deleteMany({}),
            ActiveTask.deleteMany({}),
            DailySummary.deleteMany({}),
            StaffPerformance.deleteMany({}),
            BottleneckAnalysis.deleteMany({}),
            EfficiencyTrends.deleteMany({})
        ]);
        console.log('✅ Existing data cleared');

        // Seed Time Records
        console.log('⏰ Seeding time records...');
        const timeRecords = createTimeRecords();
        await TimeRecord.insertMany(timeRecords);
        console.log(`✅ ${timeRecords.length} time records seeded`);

        // Seed Active Tasks
        console.log('⚡ Seeding active tasks...');
        const activeTasks = createActiveTasks();
        await ActiveTask.insertMany(activeTasks);
        console.log(`✅ ${activeTasks.length} active tasks seeded`);

        // Seed Daily Summaries
        console.log('📊 Seeding daily summaries...');
        const dailySummaries = createDailySummaries();
        await DailySummary.insertMany(dailySummaries);
        console.log(`✅ ${dailySummaries.length} daily summaries seeded`);

        // Seed Staff Performance
        console.log('📈 Seeding staff performance metrics...');
        const staffPerformance = createStaffPerformance();
        await StaffPerformance.insertMany(staffPerformance);
        console.log(`✅ ${staffPerformance.length} staff performance records seeded`);

        // Seed Bottleneck Analysis
        console.log('🔍 Seeding bottleneck analysis...');
        const bottlenecks = createBottleneckAnalysis();
        await BottleneckAnalysis.insertMany(bottlenecks);
        console.log(`✅ ${bottlenecks.length} bottleneck analyses seeded`);

        // Seed Efficiency Trends
        console.log('📉 Seeding efficiency trends...');
        const trends = createEfficiencyTrends();
        await EfficiencyTrends.create(trends);
        console.log('✅ Efficiency trends seeded');

        // Verify counts
        const counts = await Promise.all([
            TimeRecord.countDocuments(),
            ActiveTask.countDocuments(),
            DailySummary.countDocuments(),
            StaffPerformance.countDocuments(),
            BottleneckAnalysis.countDocuments(),
            EfficiencyTrends.countDocuments()
        ]);

        console.log('\n📊 Seeding Summary:');
        console.log(`   Time Records: ${counts[0]}`);
        console.log(`   Active Tasks: ${counts[1]}`);
        console.log(`   Daily Summaries: ${counts[2]}`);
        console.log(`   Staff Performance: ${counts[3]}`);
        console.log(`   Bottleneck Analysis: ${counts[4]}`);
        console.log(`   Efficiency Trends: ${counts[5]}`);

        // Task type breakdown
        const taskBreakdown = await TimeRecord.aggregate([
            { $match: { isInProgress: false } },
            {
                $group: {
                    _id: "$taskType",
                    count: { $sum: 1 },
                    avgDuration: { $avg: "$duration" },
                    avgVariance: { $avg: "$efficiency.variancePercent" }
                }
            },
            { $sort: { count: -1 } }
        ]);

        if (taskBreakdown.length > 0) {
            console.log('\n📈 Task Type Breakdown:');
            taskBreakdown.forEach(item => {
                console.log(`   ${item._id}: ${item.count} tasks, avg ${Math.round(item.avgDuration)}min, variance ${Math.round(item.avgVariance)}%`);
            });
        }

        // Staff productivity summary
        const staffProductivity = await StaffPerformance.aggregate([
            {
                $group: {
                    _id: null,
                    avgTodayEfficiency: { $avg: "$today.efficiency" },
                    totalTodayTasks: { $sum: "$today.tasksCompleted" },
                    avgWeekEfficiency: { $avg: "$week.averageEfficiency" }
                }
            }
        ]);

        if (staffProductivity.length > 0) {
            console.log('\n👥 Staff Productivity:');
            console.log(`   Average Today Efficiency: ${Math.round(staffProductivity[0].avgTodayEfficiency)}%`);
            console.log(`   Total Tasks Today: ${staffProductivity[0].totalTodayTasks}`);
            console.log(`   Average Weekly Efficiency: ${Math.round(staffProductivity[0].avgWeekEfficiency)}%`);
        }

        // Active tasks count
        const activeCount = await ActiveTask.countDocuments();
        console.log(`\n⚡ Currently Active Tasks: ${activeCount}`);

        console.log('\n✅ Time Management seeding completed successfully!');

    } catch (error) {
        console.error('❌ Error seeding time management:', error);
    } finally {
        // Close MongoDB connection
        await mongoose.connection.close();
        console.log('🔌 MongoDB connection closed');
    }
}

